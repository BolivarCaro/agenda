    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nombre = txtNombreCliente.getText();
        String apellido = txtApellidoCliente.getText();
        String telefono = txtTelefonoCliente.getText();
        String email = txtEmailCliente.getText();
        boolean seInserto = controlador.insertCliente(nombre, apellido, telefono, email);
        if(seInserto){
            JOptionPane.showMessageDialog(this, "Se a creado el cliente");
        }else{
            JOptionPane.showMessageDialog(this, "no se a creado tu ");
        }
        llenarTablaClientes();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        int id = Integer.parseInt(txtIdCliente.getText());
        String nombre = txtNombreCliente.getText();
        String apeliido = txtApellidoCliente.getText();
        String telefono = txtTelefonoCliente.getText();
        String email = txtEmailCliente.getText();
        
        boolean seActualizo = controlador.UpdateCliente(id, nombre, apeliido,
                telefono, email);
        if(seActualizo){
            JOptionPane.showMessageDialog(this, "se actualiz√≥ los datos");
        }
        else{
            JOptionPane.showMessageDialog(this, "no se actualizaron los datos");
        }
        llenarTablaClientes();
        
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnBuscarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarClienteActionPerformed
        int id = Integer.parseInt(txtIdCliente.getText());
        Cliente cliente = controlador.findCliente(id);
        if(cliente == null){
            JOptionPane.showMessageDialog(this, "cliente no encontrado");
        }
        else{
            txtNombreCliente.setText(cliente.nombre);
            txtApellidoCliente.setText(cliente.apellido);
            txtTelefonoCliente.setText(cliente.telefono);
            txtEmailCliente.setText(cliente.email);
        }
    }//GEN-LAST:event_btnBuscarClienteActionPerformed

    private void tblClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblClientesMouseClicked
        int fila = tblClientes.getSelectedRow();
        int id = (int) tblClientes.getValueAt(fila, 0);
        Cliente cliente = controlador.findCliente(id);
        txtNombreCliente.setText(cliente.nombre);
        txtApellidoCliente.setText(cliente.apellido);
        txtTelefonoCliente.setText(cliente.telefono);
        txtEmailCliente.setText(cliente.email);
    }//GEN-LAST:event_tblClientesMouseClicked

    private void btnDeleteClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteClienteActionPerformed
        int id = Integer.parseInt(txtIdCliente.getText());
        if(JOptionPane.showConfirmDialog(this, "Desea eliminar el municipio") == 0){
            if(controlador.deleteCliente(id)){
                JOptionPane.showMessageDialog(this, "El contacto fue eliminado");
            }
            else{
                JOptionPane.showMessageDialog(this, "El contacto no fue eliminado");
            }
        }
        llenarTablaClientes();
    }//GEN-LAST:event_btnDeleteClienteActionPerformed

    
    public void llenarTablaClientes(){
        ArrayList<Cliente> listaClientes = controlador.SelectCliente();
        int filas = listaClientes.size();
        int columnas = 5;
        Object[][] datos = new Object[filas][columnas];
        int i = 0;
        for(Cliente cliente : listaClientes){
            datos [i] [0] = cliente.id;
            datos [i] [1] = cliente.nombre;
            datos [i] [2] = cliente.apellido;
            datos [i] [3] = cliente.telefono;
            datos [i] [4] = cliente.email;
            i++;
        }
        Object[] encabezados = {"Id", "nombre", "apellido", "telefono", "email"};
        DefaultTableModel modelo = new DefaultTableModel(datos, encabezados);
        tblClientes.setModel(modelo);
    }
    
